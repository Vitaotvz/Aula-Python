# Exercício 19 - Uma escola com cursos em regime semestral, realiza duas avaliações durante o semestre e calcula a média do aluno, da seguinte maneira:

p1 = float(input('Digite o valor da primeira avaliação: '))
p2 = float(input('Digite o valor da segunda avaliaão: '))

media = (p1 (2 * p2) / 3)

if (media >= 5):
    print ('Aprovado!')
else:
    print ('Reprovado!')
