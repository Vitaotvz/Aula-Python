# Entrar via teclado com a base e a altura de um retângulo, calcular e exibir sua área.

b = int(input('Digite o valor da base do retângulo: '))

a = int(input('Digite o valor da altura do retângulo: '))

area = b * a

print(f'A aérea do retângulo entre a base {b} e a altura {a} com resultado: {area}') 