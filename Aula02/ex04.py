# Calcular e exibir a média aritmética de quatro valores quaisquer que serão digitados.

num1 = float(input("Digite o valor da primeira nota: "))

num2 = float(input("Digite o valor da segunda nota: "))

num3 = float(input("Digite o valor da terceira nota: "))

num4 = float(input("Digite o valor da ultima nota: "))

media = (num1 + num2 + num3 + num4 ) / 4

print(f'A média aritmética é: {media:.2f}')


